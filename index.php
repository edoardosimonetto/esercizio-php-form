   <h2>Lo scoiattolo è l'animale più noto e simpatico tra i roditori, non è aggressivo ma non ama la compagnia di estranei, da cui sfugge e si nasconde molto bene. Ha dimensioni medie. La sua lughezza varia da 35-45 cm, di cui 15-20 cm di coda. pesa all'incirca 230-450 g. Ha un corpo agile, slanciato e abile nell'arrampicarsi sugli
