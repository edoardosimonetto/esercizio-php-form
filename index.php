          <h2>L'orso è un grosso animale(65-1000 kg a seconda della specie), dotato di una folta pelliccia. Ha zampe grosse e una
coda piuttosto corta. È diffuso in tutto il mondo, dalle foreste al polo nord.
L'orso è onnivoro, cioè mangia di tutto dalla frutta alla carne al pesce. Mangia anche licheni, radici e bacche. È goloso
di miele.</h2>
