    <h2>Il corpo del camoscio si è perfettamente adattato al suo ambiente. Le sue zampe sono robuste e muscolose ed il passo
è molto lungo perché è privo di clavicole che gli consentono di saltare con grande facilità sulle pareti rocciose delle
vette. Inoltre la conformazione particolare degli arti e la disposizione obliqua delle scapole gli procura un effetto molla.</h2>
