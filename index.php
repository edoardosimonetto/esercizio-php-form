<h2>Il cervo</h2>
 <div class="col-sm">
            <div class="container-fluid">
            <img src="img/cervo.jpg" alt="Il cervo" style="width:220px;height:143px;">
          </div>
    <h2>Il cervo è un mammifero che ha 2 corna (o per meglio dire "palchi" o "trofei"), 4 zampe e può essere lungo 1,90 m e
pesare 250 kg. Una caratteristica molto curiosa è che il cervo perde ogni anno i palchi. Essi ricresceranno nell'identica
forma e posizione dell'anno precedente ma con una ramificazione in più.
Negli individui più deboli i palchi pesano 9-10 kg ma nei più vecchi possono pesare 16-18 kg, i cervi maschi si
"vantano" delle loro corna e le utilizzano anche per attirare le femmine. Particolare curioso: quando un animale si
ferisce la parte inferiore della zampa destra o sul bordo dello zoccolo, le sue corna crescono molto meno da quel lato.</h2>
<h2>Il camoscio</h2>
<div class="col-sm">
            <div class="container-fluid">
            <img src="img/camoscio.jpg" alt="Il cervo" style="width:220px;height:143px;">
